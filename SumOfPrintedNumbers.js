// defined a variable called sum and equal it to 0
// for loop from 0 to 135
// add each number to the variable sum
// print the number and the sum

var sum = 0;
for(var i=0; i<=135; i++){
    sum = sum + i;
    // sum += i
    console.log('Number is: '+ i + ' Sum: '+ sum);
};

// i=0 => Number is: 0 Sum: 0
// i=1 => Number is: 1 Sum: 1
// i=2 => Number is: 2 Sum: 3
